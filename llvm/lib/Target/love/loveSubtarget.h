#ifndef LLVM_LIB_TARGET_love_loveSUBTARGET_H
#define LLVM_LIB_TARGET_love_loveSUBTARGET_H

#include "loveFrameLowering.h"
#include "loveISelLowering.h"
#include "loveInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "loveGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class loveSubtarget : public loveGenSubtargetInfo {
  virtual void anchor();

  loveRegisterInfo RegInfo;
  loveInstrInfo InstrInfo;
  loveFrameLowering FrameLowering;
  loveTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  loveSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);

  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const loveInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const loveFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const loveTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const loveRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_love_loveSUBTARGET_H
